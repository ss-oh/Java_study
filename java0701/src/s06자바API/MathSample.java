package s06자바API;

public class MathSample {
	public static void main(String[] args) {

		System.out.println( Math.abs(-10) ); // 절대값
		System.out.println( Math.ceil(10.1) ); // 올림
		System.out.println( Math.floor(10.9) ); // 내림
		System.out.println( Math.round(10.5) ); // 반올림
		
		System.out.println( Math.max(5, 10) ); // 최대값
		System.out.println( Math.min(5, 10) ); // 최소값
		
	}
}
